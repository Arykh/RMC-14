using System.Linq;
using Content.Shared._RMC14.Medical.CryoCell;
using Robust.Client.AutoGenerated;
using Robust.Client.Graphics;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client._RMC14.Medical.CryoCell;

[GenerateTypedNameReferences]
public sealed partial class CryoCellWindow : DefaultWindow
{
    private static readonly StyleBoxFlat StyleGood = new(Color.FromHex("#408040"));
    private static readonly StyleBoxFlat StyleWarning = new(Color.FromHex("#A0A030"));
    private static readonly StyleBoxFlat StyleDanger = new(Color.FromHex("#A04040"));
    private static readonly StyleBoxFlat StyleCryo = new(Color.FromHex("#406080"));

    private CryoCellBui? _bui;

    public CryoCellWindow()
    {
        RobustXamlLoader.Load(this);

        PowerButton.OnPressed += _ => _bui?.TogglePower();
        EjectButton.OnPressed += _ => _bui?.Eject();
        AutoEjectButton.OnPressed += _ => _bui?.ToggleAutoEject();
        NotifyButton.OnPressed += _ => _bui?.ToggleNotify();
        EjectBeakerButton.OnPressed += _ => _bui?.EjectBeaker();
    }

    public void SetBui(CryoCellBui bui)
    {
        _bui = bui;
    }

    public void UpdateState(CryoCellBuiState state)
    {
        var hasOccupant = state.Occupant != null;
        EmptyPanel.Visible = !hasOccupant;
        OccupantPanel.Visible = hasOccupant;

        UpdateCellSection(state);

        if (!hasOccupant)
        {
            UpdateBeakerSection(state);
            return;
        }

        OccupantNameLabel.Text = state.OccupantName ?? string.Empty;
        UpdateStatus(state);
        UpdateHealth(state);
        UpdateBodyTemperature(state);
        UpdateDamageBars(state);
        UpdateBeakerSection(state);
    }

    private void UpdateCellSection(CryoCellBuiState state)
    {
        PowerButton.Pressed = state.IsOn;
        PowerButton.Text = state.IsOn
            ? Loc.GetString("rmc-cryo-cell-power-on")
            : Loc.GetString("rmc-cryo-cell-power-off");

        EjectButton.Disabled = state.Occupant == null;

        AutoEjectButton.Pressed = state.AutoEject;
        AutoEjectButton.Text = state.AutoEject
            ? Loc.GetString("rmc-cryo-cell-autoeject")
            : Loc.GetString("rmc-cryo-cell-manual");

        NotifyButton.Pressed = state.ReleaseNotice;
        NotifyButton.Text = state.ReleaseNotice
            ? Loc.GetString("rmc-cryo-cell-notify")
            : Loc.GetString("rmc-cryo-cell-silent");
    }

    private void UpdateStatus(CryoCellBuiState state)
    {
        switch (state.OccupantState)
        {
            case CryoCellOccupantMobState.Alive:
                StatusLabel.Text = Loc.GetString("rmc-cryo-cell-status-alive");
                StatusLabel.ModulateSelfOverride = Color.Green;
                break;
            case CryoCellOccupantMobState.Critical:
                StatusLabel.Text = Loc.GetString("rmc-cryo-cell-status-critical");
                StatusLabel.ModulateSelfOverride = Color.Yellow;
                break;
            case CryoCellOccupantMobState.Dead:
                StatusLabel.Text = Loc.GetString("rmc-cryo-cell-status-dead");
                StatusLabel.ModulateSelfOverride = Color.Red;
                break;
            default:
                StatusLabel.Text = string.Empty;
                StatusLabel.ModulateSelfOverride = null;
                break;
        }
    }

    private void UpdateHealth(CryoCellBuiState state)
    {
        HealthBar.MaxValue = Math.Max(state.MaxHealth, 1f);
        HealthBar.Value = Math.Max(state.Health, 0f);
        HealthBarText.Text = $"{(int) state.Health} / {(int) state.MaxHealth}";
        HealthBar.ForegroundStyleBoxOverride = state.Health > 0 ? StyleGood : StyleDanger;
    }

    private void UpdateBodyTemperature(CryoCellBuiState state)
    {
        BodyTemperatureBar.Value = state.BodyTemperature;
        BodyTemperatureText.Text = $"{(int) state.BodyTemperature} K";
        BodyTemperatureBar.ForegroundStyleBoxOverride = state.BodyTemperature switch
        {
            < 170f => StyleCryo,
            < 273f => StyleGood,
            _ => StyleWarning
        };
    }

    private void UpdateDamageBars(CryoCellBuiState state)
    {
        SetDamageBar(BruteBar, BruteBarText, state.BruteLoss);
        SetDamageBar(BurnBar, BurnBarText, state.BurnLoss);
        SetDamageBar(ToxinBar, ToxinBarText, state.ToxinLoss);
        SetDamageBar(OxygenBar, OxygenBarText, state.OxyLoss);
    }

    private static void SetDamageBar(ProgressBar bar, Label label, float value)
    {
        bar.Value = value;
        label.Text = $"{(int) value}";
        bar.ForegroundStyleBoxOverride = value switch
        {
            <= 0f => StyleGood,
            <= 25f => StyleWarning,
            _ => StyleDanger
        };
    }

    private void UpdateBeakerSection(CryoCellBuiState state)
    {
        EjectBeakerButton.Disabled = !state.IsBeakerLoaded;

        if (!state.IsBeakerLoaded || state.BeakerContents.Length == 0)
        {
            BeakerStatusLabel.Visible = true;
            BeakerStatusLabel.Text = Loc.GetString("rmc-cryo-cell-beaker-empty");
            BeakerContentsLabel.Visible = false;
            BeakerContentsLabel.Text = string.Empty;
            return;
        }

        BeakerStatusLabel.Visible = false;
        BeakerContentsLabel.Visible = true;
        BeakerContentsLabel.Text = string.Join("\n",
            state.BeakerContents.Select(r => $"{r.Name}: {(int) r.Volume} u"));
    }
}
